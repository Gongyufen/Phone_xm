package shitil;

public class C_fombination {
	private int c_fid;
	private int c_rid;
	private int c_gid;
	public C_fombination(int c_fid, int c_rid, int c_gid) {
		super();
		this.c_fid = c_fid;
		this.c_rid = c_rid;
		this.c_gid = c_gid;
	}
	public int getC_fid() {
		return c_fid;
	}
	public void setC_fid(int c_fid) {
		this.c_fid = c_fid;
	}
	public int getC_rid() {
		return c_rid;
	}
	public void setC_rid(int c_rid) {
		this.c_rid = c_rid;
	}
	public int getC_gid() {
		return c_gid;
	}
	public void setC_gid(int c_gid) {
		this.c_gid = c_gid;
	}
	
}
