package shitil;

public class C_shopping {
	private int c_sid;
	private int c_did;
	private int c_aid;
	private int c_mid;
	private int c_rid;
	private int c_state;
	public C_shopping(int c_sid, int c_did, int c_aid, int c_mid, int c_rid, int c_state) {
		super();
		this.c_sid = c_sid;
		this.c_did = c_did;
		this.c_aid = c_aid;
		this.c_mid = c_mid;
		this.c_rid = c_rid;
		this.c_state = c_state;
	}
	public int getC_sid() {
		return c_sid;
	}
	public void setC_sid(int c_sid) {
		this.c_sid = c_sid;
	}
	public int getC_did() {
		return c_did;
	}
	public void setC_did(int c_did) {
		this.c_did = c_did;
	}
	public int getC_aid() {
		return c_aid;
	}
	public void setC_aid(int c_aid) {
		this.c_aid = c_aid;
	}
	public int getC_mid() {
		return c_mid;
	}
	public void setC_mid(int c_mid) {
		this.c_mid = c_mid;
	}
	public int getC_rid() {
		return c_rid;
	}
	public void setC_rid(int c_rid) {
		this.c_rid = c_rid;
	}
	public int getC_state() {
		return c_state;
	}
	public void setC_state(int c_state) {
		this.c_state = c_state;
	}
	
}
