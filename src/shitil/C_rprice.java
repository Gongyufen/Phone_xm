package shitil;

public class C_rprice {
	private int c_rid;
	private double c_rsprice;
	public C_rprice(int c_rid, double c_rsprice) {
		super();
		this.c_rid = c_rid;
		this.c_rsprice = c_rsprice;
	}
	public int getC_rid() {
		return c_rid;
	}
	public void setC_rid(int c_rid) {
		this.c_rid = c_rid;
	}
	public double getC_rsprice() {
		return c_rsprice;
	}
	public void setC_rsprice(double c_rsprice) {
		this.c_rsprice = c_rsprice;
	}
	
}
